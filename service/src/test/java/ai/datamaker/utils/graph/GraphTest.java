package ai.datamaker.utils.graph;

import ai.datamaker.utils.graph.Graph;
import org.junit.jupiter.api.Test;

class GraphTest {

    @Test
    void topologicalSort() {
        Graph g=new Graph(6);
        g.addEdge(5, 2);
        g.addEdge(5, 0);
        g.addEdge(4, 0);
        g.addEdge(4, 1);
        g.addEdge(2, 3);
        g.addEdge(3, 1);
        System.out.println("Following is a Topological Sort");

        // Print topological order
        for (int i : g.topologicalSort()) {
            System.out.print(i + " ");
        }
    }
}